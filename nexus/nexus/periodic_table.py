@dataclass(frozen=True)
class ElementData:
    """Dataclass for storing element data."""
    symbol: str
    atomic_number: int
    atomic_weight: float
    group: int
#end class ElementData


class Element(ElementData, Enum):
    """Enumeration of all elements in the periodic table.

    Attributes
    ----------
    symbol : str
        In titlecase (H, He, ...)
    atomic_number : int
        Use 0 for a dummy element 
        (symbol "Xx", name "Unknown", all properties zero)
    atomic_weight : float
        Average atomic weight in amu.
    group : int
        Group of the element on the periodic table.
        Lanthanides and Actinides are 0.

    Notes
    -----
    We do not include all of the data for each element in this Enum
    because it does not make sense to instantiate each member with every
    value at runtime.

    The other properties are accessible the same way as the ones stored
    here, but are stored in ``@property``-decorated functions.
    """

    def __new__(
        cls,
        symbol: str,
        atomic_number: int,
        atomic_weight: float,
        group: int,
        atomic_radius: float,
    ):
        element = ElementData.__new__(cls)
        element._value_ = ElementData(
            symbol,
            atomic_number,
            atomic_weight,
            group,
        )
        return element

